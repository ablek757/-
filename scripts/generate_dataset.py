dataset = []
global_id = 1

for intent, count in target_counts.items():
    pool = samples[intent]
    # 如果pool不够count条，循环使用（但优先不重复）
    if len(pool) >= count:
        selected = random.sample(pool, count)
    else:
        # 先用完全部，再随机补足
        selected = pool.copy()
        remaining = count - len(pool)
        selected += random.choices(pool, k=remaining)

    for text in selected:
        dataset.append({
            "id": global_id,
            "text": text,
            "intent": intent,
            "intent_id": intent_id_map[intent],
            "intent_cn": intent_cn_map[intent],
        })
        global_id += 1

# 打乱顺序
random.shuffle(dataset)

